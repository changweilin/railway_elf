// Validates the merged rail-data line shapes against a committed snapshot.
//
// Run:
//   npm run check:shapes              — verify against scripts/line-shape-snapshot.json
//   npm run check:shapes -- --update  — overwrite the snapshot with current metrics
//
// Per line we compute three metrics:
//   - totalKm:      length of the polyline (last shape point's cumulative km).
//                   Drops are usually a sign that an upstream relation lost a
//                   sub-route (we have a known 16 % short-fall on
//                   Tokaido-Shinkansen — see the snapshot's tokaido note).
//   - monotonic:    do station kms strictly increase along stops? Catches
//                   stitching mistakes where the polyline doubles back.
//   - maxStationOffsetKm: largest haversine distance between a hand-coded
//                   station (lat/lng) and the polyline-projected position at
//                   that station's km. Spikes flag misaligned stations.
//
// Failure rules (compared to snapshot):
//   - !monotonic                                          → FAIL (always)
//   - totalKm < snapshot.totalKm * (1 - LENGTH_DROP_PCT)   → FAIL
//   - maxStationOffsetKm > snapshot.maxStationOffsetKm + OFFSET_SLACK_KM → FAIL
//   - new line not in snapshot                            → WARN, included on
//                                                          --update for next run

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const SNAPSHOT_PATH = resolve(__dirname, "line-shape-snapshot.json");

const args = new Set(process.argv.slice(2));
const UPDATE = args.has("--update") || args.has("--update-snapshot");

// Tunables. Picked so a noisy data refresh does not flap the check, while
// real regressions (e.g. losing a station or a sub-route) still trip it.
const LENGTH_DROP_PCT = 0.05;        // allow up to 5% shorter
const OFFSET_SLACK_KM = 0.5;         // allow station offset to grow by 0.5 km

// ---------------------------------------------------------------------------
// rail-data.js is an ES module post-migration; dynamic-import it directly.

const { RAIL_DATA, RailUtil } = await import("../src/rail-data.js");
if (!RAIL_DATA || !RailUtil) {
  console.error("Import failed — RAIL_DATA / RailUtil missing from rail-data.js");
  process.exit(1);
}

// ---------------------------------------------------------------------------
// Compute current metrics.

function metricsFor(line) {
  const shape = line.shape || [];
  const totalKm = shape.length ? shape[shape.length - 1].km : 0;
  let monotonic = true;
  const stations = line.stations || [];
  for (let i = 1; i < stations.length; i++) {
    if (stations[i].km < stations[i - 1].km - 1e-6) { monotonic = false; break; }
  }
  let maxStationOffsetKm = 0;
  for (const s of stations) {
    const p = RailUtil.positionAtKm(line, s.km);
    if (!p) continue;
    const d = RailUtil.haversine(p, s);
    if (d > maxStationOffsetKm) maxStationOffsetKm = d;
  }
  return {
    totalKm: round(totalKm, 3),
    monotonic,
    maxStationOffsetKm: round(maxStationOffsetKm, 3),
    shapePoints: shape.length,
    stations: stations.length,
  };
}

function round(n, d) {
  const m = 10 ** d;
  return Math.round(n * m) / m;
}

const current = {};
for (const region of Object.keys(RAIL_DATA)) {
  for (const line of RAIL_DATA[region].lines) {
    current[line.id] = { region, ...metricsFor(line) };
  }
}

// ---------------------------------------------------------------------------
// --update path: overwrite snapshot.

if (UPDATE) {
  const out = {
    note: "Auto-generated by scripts/check-line-shapes.mjs --update. The line-shape check ratchets against these values; bump only when an intentional data refresh accepts the new geometry.",
    updatedAt: new Date().toISOString(),
    rules: { lengthDropPct: LENGTH_DROP_PCT, offsetSlackKm: OFFSET_SLACK_KM },
    lines: current,
  };
  writeFileSync(SNAPSHOT_PATH, JSON.stringify(out, null, 2) + "\n", "utf8");
  console.log(`Updated ${SNAPSHOT_PATH} with ${Object.keys(current).length} line(s).`);
  process.exit(0);
}

// ---------------------------------------------------------------------------
// Verify path: compare against snapshot.

if (!existsSync(SNAPSHOT_PATH)) {
  console.error(`Missing snapshot at ${SNAPSHOT_PATH}. Run with --update to seed it.`);
  process.exit(1);
}

const snapshot = JSON.parse(readFileSync(SNAPSHOT_PATH, "utf8"));
const baseline = snapshot.lines || {};

let failures = 0;
let warnings = 0;
const fail = (msg) => { failures++; console.error(`  FAIL: ${msg}`); };
const warn = (msg) => { warnings++; console.warn(`  WARN: ${msg}`); };

console.log(`Validating ${Object.keys(current).length} line shape(s) against snapshot ${snapshot.updatedAt || "(unknown)"}\n`);

for (const [id, m] of Object.entries(current)) {
  const base = baseline[id];
  const head = `${m.region}/${id}`;
  const fmt = `total ${m.totalKm.toFixed(2).padStart(7)} km · stations ${String(m.stations).padStart(3)} · maxOffset ${m.maxStationOffsetKm.toFixed(3).padStart(6)} km · shapePts ${String(m.shapePoints).padStart(4)}`;
  if (!base) {
    warn(`${head}: new line, not in snapshot — ${fmt}. Run --update to record.`);
    continue;
  }

  // Hard rule: monotonic stations.
  if (!m.monotonic) {
    fail(`${head}: stations are non-monotonic in km`);
  }

  // Length ratchet — only catch significant shortfalls. Slight regrowth
  // (longer than snapshot) is fine and we just report.
  const minAllowed = base.totalKm * (1 - LENGTH_DROP_PCT);
  if (m.totalKm < minAllowed - 1e-3) {
    fail(`${head}: totalKm ${m.totalKm.toFixed(2)} km dropped below ${minAllowed.toFixed(2)} km (snapshot ${base.totalKm.toFixed(2)})`);
  }

  // Station offset ratchet — fail when stations drift far from the polyline.
  const maxAllowedOffset = base.maxStationOffsetKm + OFFSET_SLACK_KM;
  if (m.maxStationOffsetKm > maxAllowedOffset + 1e-3) {
    fail(`${head}: maxStationOffsetKm ${m.maxStationOffsetKm.toFixed(3)} km exceeds ${maxAllowedOffset.toFixed(3)} km (snapshot ${base.maxStationOffsetKm.toFixed(3)})`);
  }

  console.log(`  ${head.padEnd(36)} ${fmt}`);
}

console.log("");
if (failures > 0) {
  console.error(`${failures} failure(s), ${warnings} warning(s).`);
  console.error("If the regression is intentional (e.g. accepted upstream change), rerun with --update.");
  process.exit(1);
}
if (warnings > 0) {
  console.warn(`${warnings} warning(s) — snapshot is missing entries. Run --update once you're happy with the current values.`);
}
console.log("Line-shape checks passed.");
